#Multiple Assignment = alows us to assign multiple variables at the same time in one line of code

#This is traditional method
name = "Bro"
age = 18
attractive = True

print(name)
print(age)
print(attractive)


#This is multiple assignment method
name, age, attractive = "Bro", 18, True
print(name)
print(age)
print(attractive)


Spongebob = Patrick = Sandy = Squidward = 30
print(Spongebob)

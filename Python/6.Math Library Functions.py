import math

pi= 3.14
x = 1
y = 25
z = 2

print(round(pi)) #rounds off to the nearest integer
print(math.ceil(pi)) #rounds up to the nearest integer
print(math.floor(pi)) #rounds down to the nearest integer
print(abs(pi)) #returns how far the number is from zero. It is always positive
print(pow(pi ,2)) #raises the base number to a power
print(math.sqrt(420)) #returns the square root of the number specified number
print(max(x,y,z,pi)) #returns the max value of the specified numbers 
print(min(x,y,z,pi)) #returns the min value of the specified numbers